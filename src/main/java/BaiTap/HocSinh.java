public class HocSinh {
    String maHocSinh;
    String tenHocSinh;
    String monHoc;
    float diem;

    public HocSinh() {
    }

    public HocSinh(String maHocSinh, String tenHocSinh, String monHoc, float diem) {
        this.maHocSinh = maHocSinh;
        this.tenHocSinh = tenHocSinh;
        this.monHoc = monHoc;
        this.diem = diem;
    }
}
